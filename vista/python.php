        <h1>El lenguaje de programación Python: ¿Qué es?</h1>
        <p>
        Python es un lenguaje de programación de alto nivel, interpretado y generalmente considerado como fácil de aprender. Fue creado a fines de la década de 1980 por Guido van Rossum y se ha vuelto muy popular en la comunidad de desarrollo de software debido a su enfoque en la legibilidad y la simplicidad del código.</p>
        
     <h1> ¿Para que se usa?</h1>
     <p>
     Python se usa para desarrollar aplicaciones web, automatizar tareas, analizar datos, crear inteligencia artificial y machine learning, construir aplicaciones de escritorio, pruebas de software, procesamiento de lenguaje natural y más. Su simplicidad y versatilidad lo hacen valioso en una amplia gama de áreas.
     
     </p>

