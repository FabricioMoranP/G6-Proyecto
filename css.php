        <h1>El lenguaje de diseño CSS: ¿Qué es?</h1>
        <p>
        CSS (Cascading Style Sheets) es un lenguaje de hojas de estilo utilizado para definir la apariencia y el diseño de elementos en una página web. En lugar de centrarse en el contenido o la estructura, como lo hace HTML, CSS se concentra exclusivamente en la presentación visual de los elementos HTML.

Con CSS, puedes aplicar reglas y estilos a diferentes elementos de una página web, lo que permite controlar aspectos como colores, fuentes, márgenes, espaciados, tamaños y posicionamiento de elementos. Esto te permite crear páginas web atractivas y coherentes en términos de diseño.</p>
        
