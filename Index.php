                    Considerado con frecuencia el mejor y más completo futbolista, ​así como el mayor goleador del mundo,
                    ​además de uno de los mejores de todos los tiempos; ​ es uno de los futbolistas más laureados de la
                    historia, habiendo ganado, entre otras distinciones, cinco veces el Balón de Oro, cinco premios de la
                    FIFA
                    al mejor jugador del mundo y cuatro Botas de Oro. En 2020, tuvo el honor de ser elegido el mejor Jugador
