            <h1 class="article-subtitulo-h1">Cristiano Ronaldo ¿Quién es?</h1>
                <p>
                    Cristiano Ronaldo dos Santos Aveiro (Funchal, Madeira, 5 de febrero de 1985), conocido como Cristiano
                    Ronaldo, es un futbolista portugués. Juega como extremo izquierdo o delantero y su equipo actual es el
                    Al-Nassr F. C. de la Liga Profesional Saudí. Es internacional absoluto con la selección de Portugal, de
                    la cual es capitán, máximo goleador histórico y jugador con más presencias con 200 partidos, logro
                    alcanzado en las eliminatorias para la Eurocopa 2024, reconocido por el Libro Guinness de los récords​.
                </p>
