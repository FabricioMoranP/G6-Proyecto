                <h1 class="article-subtitulo-h1">El máximo goleador de la historia del Real Madrid</h1>
                <p><b>Posición en el campo: </b>Delantero</p>
                <p><b>Partidos Jugados: </b>438 oficiales</p>
                <p><b>Goles marcados: </b>451</p>
                <p><b>Internacional con Portugal: </b>154 veces</p>
                <p>
                    Cristiano Ronaldo forma parte de la leyenda del Real Madrid y siempre será recordado como uno de los
                    grandes símbolos de su historia. Fue presentado en el Santiago Bernabéu el 6 de julio de 2009,
                    acompañado por Eusebio y Alfredo Di Stéfano, y desde entonces no paró de marcar goles: 451 en 438
                    partidos oficiales con el Real Madrid (más de uno por encuentro). Además vio puerta en todas las
                    competiciones: 312 en la Liga, 105 en la Champions, 22 en la Copa del Rey, 6 en el Mundial de Clubes, 4
                    en la Supercopa de España y 2 en la Supercopa de Europa.
                </p>
                <p>
                    Nadie en la historia del club ha marcado tantos goles como el delantero portugués, cuyo palmarés en el
                    Real Madrid es impresionante: 4 Champions League, 3 Mundiales de Clubes, 3 Supercopas de Europa, 2
                    Ligas, 2 Copas del Rey y 2 Supercopas de España. A ellos suma 4 Balones de Oro, 3 Botas de Oro, 2
                    Premios The Best, 3 Premios al Mejor Jugador de la UEFA y 3 Trofeos Pichichi.
                </p>
                <h3 class="article-subtitulo-h3">Trayectoria de récords</h3>
                <p>
                    A lo largo de sus nueve temporadas como madridista, Cristiano dejó varios récords impresionantes: máximo
                    goleador de la historia del club, máximo goleador de la historia de la Copa de Europa (marcó 105 tantos
                    en Champions con nuestro equipo); máximo goleador madridista en la Liga (312); más partidos de tres o
                    más goles en la historia de la Liga (34); y más tantos en una temporada con el Real Madrid (61).
                </p>
                <p>
                    Batió también el récord de goles en una edición de la Champions (17) y fue pichichi en 6 ediciones de
                    esta competición. El presidente Florentino Pérez le calificó como “el heredero de Alfredo Di Stéfano”. Y
                    como Don Alfredo, dejó una huella imbo  rrable en la historia del mejor club del mundo.
                </p>
